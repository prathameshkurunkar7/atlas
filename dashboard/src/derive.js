// ═══════════════════════════════════════════════════════════════════════════
// derive.js — the data join at the heart of the VM-as-subject re-model.
//
// /api/state is FLAT: virtual_machines is one array, and every network/firewall
// row a VM owns lives in a *separate* anonymous array (routes, nft rules,
// neigh_proxy, ip_rules, reserved_ips, units, snapshots). The flat document
// "can't join a VM to its own rows." These pure functions do that join, so the
// VM can become the subject.
//
// PURE. No Vue, no DOM, no fetch — every function takes plain data and returns
// plain data, so it's unit-testable and reasoned about in isolation.
//
// DEFENSIVE by design (mirroring the backend's "server is a
// cache" philosophy): a missing field yields a dropped leg or a "—" cell, never
// a thrown error. The matchers key off whatever identifying tokens a VM row
// happens to carry, so they work across all three fixtures, which now share ONE
// shape (backend/server.py's build_state()) and differ only in CARDINALITY:
//   · mock/state-ordinary.json (~24 VMs) + state-scale.json (~1000 VMs) —
//     generated by mock/generate.mjs.
//   · mock/state-real.json — a live `server.py --collect` capture off a real
//     host (mock/collect-real.mjs), so nothing is idealised.
// All three carry the real host shape: nft keyed on host_veth `atlas-h<...>`
// plus IPv6 daddr/saddr, NAT written `dnat ip to X` / `snat ip to X`, reserved
// rows carry guest_ipv4, anchor is null (Scaleway, no DO anchor).
// ═══════════════════════════════════════════════════════════════════════════

const EMPTY = "—";

function uuid8(uuid) {
	return uuid ? String(uuid).slice(0, 8) : "";
}

// The set of strings that identify a VM's rows in the flat document. A row
// belongs to the VM if it mentions ANY of these. Ordered roughly host-veth →
// guest addrs; all are distinctive enough not to collide across VMs.
function vmTokens(vm) {
	const t = [];
	const push = (v) => {
		if (v != null && v !== "") t.push(String(v));
	};
	push(vm.host_veth); // real hosts: atlas-h<uuid7>
	push(vm.namespace_veth); // real hosts: atlas-n<uuid7>
	if (vm.uuid) push(`veth-${uuid8(vm.uuid)}`); // mock/scale convention
	push(vm.ipv4_guest);
	push(vm.ipv6);
	push(vm.tap_device);
	push(vm.netns);
	push(vm.reserved_ipv4);
	return t;
}

// Does `hay` (a rule string, a route dev/dest, an NDP address, …) reference any
// of the VM's tokens? Substring match — nft rules and routes embed these verbatim.
function mentions(hay, tokens) {
	if (hay == null) return false;
	const s = String(hay);
	return tokens.some((tok) => s.includes(tok));
}

// Flatten nft into {family, table, persisted, chain, type, rule} records once,
// so every matcher and the reverse index share one view of the ruleset.
function nftRecords(state) {
	const out = [];
	for (const t of state.nft_tables || []) {
		for (const c of t.chains || []) {
			for (const rule of c.rules || []) {
				out.push({
					family: t.family,
					table: t.name,
					persisted: !!t.persisted,
					chain: c.name,
					type: c.type,
					rule,
				});
			}
		}
	}
	return out;
}

// ── deriveVm(state, vm) → the joined detail object ──────────────────────────
// Best-effort: every field defaults to empty/absent rather than throwing.
export function deriveVm(state, vm) {
	const tokens = vmTokens(vm);
	const nft = nftRecords(state);
	const guest = vm.ipv4_guest;

	const routes = (state.routes || []).filter(
		(r) => mentions(r.dev, tokens) || mentions(r.dest, tokens)
	);

	// forward-chain (filter) rules that name this VM (via veth or guest v4/v6).
	const filterRules = nft
		.filter((n) => n.type === "filter" && mentions(n.rule, tokens))
		.map((n) => n.rule);

	// DNAT: a prerouting/nat rule whose TARGET is this guest's v4. Real writes
	// "dnat ip to 100.64.0.14"; mock writes "dnat to 100.64.0.14" — both contain
	// "dnat" and the guest address.
	const dnat =
		guest &&
		nft.find((n) => n.type === "nat" && /\bdnat\b/.test(n.rule) && n.rule.includes(guest))
			?.rule;

	// SNAT: a postrouting/nat rule whose SOURCE is this guest's v4.
	const snat =
		guest &&
		nft.find((n) => n.type === "nat" && /\bsnat\b/.test(n.rule) && n.rule.includes(guest))
			?.rule;

	const ndp = (state.neigh_proxy || []).find((p) => p.address === vm.ipv6) || null;
	const ipRule = (state.ip_rules || []).find((r) => r.from === guest) || null;
	const reserved = (state.reserved_ips || []).find((r) => r.attached_vm === vm.uuid) || null;
	const unit = (state.units || []).find((u) => vm.uuid && u.name.includes(vm.uuid)) || null;
	const snapshot = (state.snapshots || []).find((s) => s.uuid === vm.uuid) || null;

	return {
		vm,
		running: vm.state === "Running",
		tokens,
		routes,
		filterRules,
		dnat: dnat || null,
		snat: snat || null,
		ndp,
		ipRule,
		reserved,
		unit,
		snapshot,
		// Disk lineage. Prefer the LV origin when the backend joins
		// it; otherwise fall back to the (usually null) image name.
		diskOrigin: diskOrigin(vm),
		dataPercent: vm.disk_data_percent != null ? vm.disk_data_percent : null,
		// Per-VM size (vcpus/mem from firecracker.json).
		size: vmSize(vm),
	};
}

// "image ubuntu-24.04" / "snap mjmdd1p7fb" / "—". Mirrors App.vue's diskOrigin.
export function diskOrigin(vm) {
	if (vm.disk_origin) {
		// The column is titled "Image", so a base image shows the bare name; a
		// snapshot origin keeps its "snap " prefix since that's a distinct origin.
		const snap = vm.disk_origin.includes("-snap-");
		const name = vm.disk_origin.replace(/^atlas-(image|snap)-/, "");
		return snap ? `snap ${name}` : name;
	}
	return vm.image || EMPTY;
}

// "1 · 1024m" or "—". Mirrors App.vue's vmSize.
export function vmSize(vm) {
	if (vm.vcpus == null && vm.mem_mib == null) return EMPTY;
	return `${vm.vcpus ?? "?"} · ${vm.mem_mib ?? "?"}m`;
}

// ── Disk rows ───────────────────────────────────────────────────────────────
// A disk row belongs to a VM when it's a device-mapper / LVM volume backing a
// VM's root or a snapshot of one. lsblk dumps EVERY block device — on a busy
// host that's hundreds of per-VM LVs (`atlas-atlas--vm--<uuid>`, `…--snap--…`,
// plus dm `atlas-vm-<uuid>-clone`) that swamp the host-primitive disks the
// operator actually reasons about (the NVMe drives, the RAID md's, partitions,
// the thin pool's own meta/data). This flags the per-VM rows so the Disks view
// can fold them behind a toggle, exactly like Units/Processes fold VM units.
// Base images (`…--image--…`) and pool plumbing stay visible: they're host
// storage, not one VM's disk.
export function diskIsVmRow(disk) {
	const name = disk?.name || "";
	if (disk?.kind !== "lvm" && disk?.kind !== "dm") return false;
	if (name.includes("pool")) return false; // thin-pool meta/data = host plumbing
	if (name.includes("image")) return false; // base images = shared host storage
	// lsblk's dm names double every dash (LV `atlas-vm-x` → `atlas-atlas--vm--x`),
	// so match on the un-doubled token: a vm or snap volume.
	return name.includes("vm") || name.includes("snap");
}

// ── derivePath(detail) → the directed connectivity legs ─────────────────────
// Returns an ordered list of legs, or null for a stopped VM (whose live network
// rows are genuinely torn down — see the "Stopped" note in §4). Each leg is
// { dir, from, hop?, to, xf, key? } where `key:true` marks the darkest-ink node
// (the public reserved IP) and `xf` is the transform tag.
export function derivePath(detail) {
	if (!detail || !detail.running) return null;
	const { vm, reserved } = detail;
	const legs = [];

	// In v6 — always present for a running VM. Routed /128, no NAT.
	if (vm.ipv6) {
		legs.push({ dir: "In v6", from: vm.ipv6, to: "guest", xf: "routed /128" });
	}

	const reservedAddr = reserved?.address || vm.reserved_ipv4 || null;

	if (reservedAddr) {
		// In v4 — DNAT. DO model hops through the anchor; Scaleway is direct
		// (anchor null on Scaleway), so the hop is simply omitted.
		const inLeg = {
			dir: "In v4",
			from: reservedAddr,
			key: true,
			to: vm.ipv4_guest || "guest",
			xf: "DNAT",
		};
		if (reserved?.anchor) inLeg.hop = reserved.anchor;
		legs.push(inLeg);

		// Out v4 — SNAT back to the reserved public IP.
		legs.push({
			dir: "Out v4",
			from: vm.ipv4_guest || "guest",
			to: reservedAddr,
			key: true,
			xf: "SNAT",
		});
	} else if (vm.ipv4_guest) {
		// No reserved IP: outbound v4 is masqueraded behind the uplink.
		const uplink = detail.uplink || "eth0";
		legs.push({
			dir: "Out v4",
			from: vm.ipv4_guest,
			to: uplink,
			xf: "masquerade",
		});
	}

	return legs;
}

// A single prose sentence summarising the VM's forward-chain policy, e.g.
// "veth allowed both ways · 100.64.0.14 accepted · metadata 169.254.169.254 blocked".
// Best-effort readout of the filter rules the join found, plus the host-wide
// metadata drop (which protects every VM, so it's worth stating per-VM).
export function deriveFilterSentence(state, detail) {
	if (!detail || !detail.running) return "";
	const rules = detail.filterRules || [];
	const parts = [];

	const hasIn = rules.some((r) => /iifname|saddr/.test(r) && /\baccept\b/.test(r));
	const hasOut = rules.some((r) => /oifname|daddr/.test(r) && /\baccept\b/.test(r));
	if (hasIn && hasOut) parts.push("veth allowed both ways");
	else if (hasIn) parts.push("inbound allowed");
	else if (hasOut) parts.push("outbound allowed");

	if (detail.vm.ipv4_guest && rules.some((r) => r.includes(detail.vm.ipv4_guest))) {
		parts.push(`${detail.vm.ipv4_guest} accepted`);
	}

	// The metadata drop is a host-wide forward rule; surface it as the protection
	// it is. Detect it once from the ruleset.
	const meta = nftRecords(state).find(
		(n) => n.type === "filter" && /169\.254\.169\.254/.test(n.rule) && /\bdrop\b/.test(n.rule)
	);
	if (meta) parts.push("metadata 169.254.169.254 blocked");

	return parts.join(" · ");
}

// The one sentence a stopped VM's detail shows in place of a path.
export const STOPPED_SENTENCE =
	"Stopped — veth, routes, NAT and firewall rules are torn down. Disk LV persists.";

// ── vmByRule(state) → reverse index ─────────────────────────────────────────
// Given the flat rows, returns lookups that map a row back to its owning VM
// uuid (or null for host fabric). Same matching logic as deriveVm, inverted, so
// the Firewall / Network sections can render a "→ <uuid8>" back-link.
export function vmByRule(state) {
	const vms = state.virtual_machines || [];
	const indexed = vms.map((vm) => ({ vm, tokens: vmTokens(vm) }));

	const owner = (hay) => {
		if (hay == null) return null;
		const s = String(hay);
		for (const { vm, tokens } of indexed) {
			if (tokens.some((tok) => s.includes(tok))) return vm.uuid;
		}
		return null;
	};

	return {
		// nft rule string → uuid|null
		rule: (rule) => owner(rule),
		// route row → uuid|null (match on dev or dest)
		route: (r) => owner(r?.dev) || owner(r?.dest),
		// ndp row → uuid|null
		ndp: (p) => {
			const vm = vms.find((v) => v.ipv6 === p?.address);
			return vm ? vm.uuid : null;
		},
		// ip-rule row → uuid|null
		ipRule: (r) => {
			const vm = vms.find((v) => v.ipv4_guest === r?.from);
			return vm ? vm.uuid : null;
		},
		// reserved-ip row → uuid|null (it already carries attached_vm)
		reserved: (r) => r?.attached_vm || null,
		// unit row → uuid|null (firecracker-vm@<uuid>.service)
		unit: (u) => {
			const vm = vms.find((v) => v.uuid && u?.name?.includes(v.uuid));
			return vm ? vm.uuid : null;
		},
		// generic: any string
		any: owner,
	};
}

// ── hostResidue(state) → the rows NOT claimed by any VM ──────────────────────
// Whatever no VM owns is "host fabric": default routes, the masquerade-all rule,
// the metadata-drop rule, the mgmt-input chain, atlas-pool.service, nftables.
// service, migrate-forwarder units, the uplink interface, etc. The section
// tables render these plain (no back-link). Returns unclaimed rows per section.
export function hostResidue(state) {
	const idx = vmByRule(state);
	return {
		routes: (state.routes || []).filter((r) => !idx.route(r)),
		neigh_proxy: (state.neigh_proxy || []).filter((p) => !idx.ndp(p)),
		ip_rules: (state.ip_rules || []).filter((r) => !idx.ipRule(r)),
		nftRules: nftRecords(state).filter((n) => !idx.rule(n.rule)),
		units: (state.units || []).filter((u) => u.kind !== "vm" && !idx.unit(u)),
	};
}

